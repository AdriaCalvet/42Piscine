#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int	r;
	int	i;

	i = 0;
	r = 1;
	while (str[i] != '\0' && r == 1)
	{
		if (!(str[i] >= 'A' && str[i] <= 'Z'))
			if (!(str[i] >= 'a' && str[i] <= 'z'))
				r = 0;
		i++;
	}
	return (r);
}
